v = input('digite algo: ')
print('O tipo primitivo desse valor é:', type(v))
print('So tem espaços?', v.isspace())
print('É um numero?', v.isnumeric())
print('É alfabetico?', v.isalpha())
print('É alfanúmerico?', v.isalnum())
print('Está em maiúsculas?', v.isupper())
print('Está em minúsculas?', v.islower())
print('Está capitalizado?', v.istitle())


